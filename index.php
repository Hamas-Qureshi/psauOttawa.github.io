              PSA UO - Mission

              To help students gain a higher perspective and knowledge on the culture of Pakistan by interacting through
              student-run events based at the University of Ottawa.

              - To propagate and promote Pakistani culture and heritage among Pakistani-Canadians
              - To carry on social and cultural activities.
              - To provide an opportunity for its members to learn and nurture the Pakistani language.
              - To provide a means for interaction among various generations of Pakistani-Canadians and individuals of
              non-Pakistani descent in the uOttawa community interested in Pakistani culture.
              - PSA would not enforce religious beliefs on any members in anyway.
              - PSA at uOttawa will operate in compliance with all relevant uOttawa policies.
              PSA UO - Mission

              PSA is a group at the University of Ottawa whose mandate is to promote social and cultural events in order
              to allow members to embrace the Pakistani tradition. The events we organize are mainly for the sheer
              enjoyment of the group members and include but are not limited to activities, formals, or social
              gatherings. The events we organize benefit the SFUO and the University of Ottawa by building a bridge
              between other SFUO clubs & associations by helping students interact with others and help gain a better
              knowledge on what the campus has to offer.


